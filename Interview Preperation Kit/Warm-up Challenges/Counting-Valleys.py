def countingValleys(steps, path):
    # Write your code here
    count_d = 0
    count_u = 0
    for i in path:
        if i == 'U':
            count_u += 1
            if count_u == 0:
                count_d += 1
        else:
            count_u -= 1
    return count_d
      

